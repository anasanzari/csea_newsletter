        <h2>Gadget Gizmo</h2>
        <p class="author">- <em>Anshul Chokhani & Piyush Bhopalka</em></p>
        <div class="container-fluid">
          <div class="row">
            <div class="col-md-6">
              <p>The <strong>Asus Chromebit</strong> is a HDMI dongle, which can be connected to a display (TV/ Monitor) to bring Chrome OS functionality to them. Sometime back, Google had unveiled Chromecast, a HDMI dongle which was basically extending a second screen from your phone, tablet or your Chromebook onto the big screen. However, this time with the partnership with Asus, Google has released a full OS on the pendrive-like device. Just plug it in the HDTV and start using. It has been available in India since January for Rs 7,999. </p>
              <p><strong>Asus Chromebit specifications and features: </strong></p>
              <p>On the hardware front, it is powered by a Rockchip 3288 ARM-based processor paired with 2GB of RAM and 16GB of onboard storage. The Chromebit also includes connectivity options such as Wi-Fi 802.11ac, Bluetooth 4.0 and a USB 2.0 port.
                The USB 2.0 port on the dongle lets you connect peripherals such as a USB mouse or keyboard, a pen drive or an external hard disk.</p>

